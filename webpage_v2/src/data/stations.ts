// System display order
export const SYSTEM_ORDER: TransitSystem[] = ['NJT', 'PATH', 'LIRR', 'MNR', 'SUBWAY', 'BART', 'AMTRAK', 'PATCO'];

// Helper functions
export function getStationByCode(code: string): Station | undefined {
  return STATIONS.find(s => s.code === code);
}

export function searchStations(query: string): Station[] {
  if (!query) return [];
  const q = query.toLowerCase();
  return STATIONS
    .filter(s =>
      s.name.toLowerCase().includes(q) ||
      s.code.toLowerCase().includes(q)
    )
    .slice(0, 15);
}

export function getGroupedPrimaryStations(): { system: TransitSystem; name: string; stations: Station[] }[] {
  return SYSTEM_ORDER.map(system => ({
    system,
    name: SYSTEM_NAMES[system],
    stations: PRIMARY_STATIONS[system]
      .map(code => getStationByCode(code))
      .filter((s): s is Station => s !== undefined),
  })).filter(group => group.stations.length > 0);
}
